const parseJSON = <T,>(value: string | null, fallback: T): T => {
  try {
    return value ? (JSON.parse(value) as T) : fallback;
  } catch {
    return fallback;
  }
};

// Delete a thread from the backend (for authenticated users)
const API_BASE_URL = import.meta.env.VITE_API_BASE_URL ?? `${window.location.protocol}//${window.location.hostname}:8000`;
async function deleteThreadFromBackend(threadId: string): Promise<void> {
  const token = localStorage.getItem("auth_token");
  const headers: Record<string, string> = { "Content-Type": "application/json" };
  if (token) {
    headers["Authorization"] = `Bearer ${token}`;
  }
  const res = await fetch(`${API_BASE_URL}/auth/threads/${encodeURIComponent(threadId)}`, {
    method: "DELETE",
    headers,
  });
  if (!res.ok && res.status !== 404) {
    // 404 is ok - thread may not exist on backend
    const message = await res.text();
    throw new Error(message || "Failed to delete conversation");
  }
}

